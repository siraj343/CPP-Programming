#include<iostream>
using namespace std;

/*
Question 3 (Moderate)

Take two integers as input and print:
1. Sum
2. Difference
3. Product
4. Quotient
*/

int main() {
    int a,b;
    cin>>a>>b;
    cout <<"sum: "<<a+b<<endl;
    cout <<"difference: "<<a-b<<endl;
    cout <<"product: "<<a*b<<endl;
    cout <<"quotient: "<<a/b;
}